#include "push_swap.h"

int	ft_isdigit(char *s)
{
	int	i;

	i = 0;
	if (s[0] == '+' || s[0] == '-')
		i++;
	while (s[i])
	{
		if (s[i] < '0' || s[i] > '9')
			return (0);
		i++;
	}
	return (1);
}

int	ft_digit(char s)
{
	if (s < '0' || s > '9')
		return (0);
	return (1);
}
